public class student extends Person{
	private String stdNum;
	private String clRoom;
	public student() {
		
	}
	
	public String getStdNum() {
		return stdNum;
	}

	public void setStdNum(String stdNum) {
		this.stdNum = stdNum;
	}

	public String getClRoom() {
		return clRoom;
	}

	public void setClRoom(String clRoom) {
		this.clRoom = clRoom;
	}

}
